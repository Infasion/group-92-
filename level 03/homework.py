# დავალება 4: snake_case
# ==========================================

# snake_case არის ცვლადების სახელდების სტილი,
# სადაც სიტყვები ერთმანეთთან დაკავშირებულია ქვედა ტირეთი (_),
# ხოლო ყველა ასო პატარაა.
# მაგალითი: my_variable, first_name, user_age
# Python-ში snake_case არის სტანდარტული/რეკომენდებული სტილი.

# ცვლადები snake_case-ით:
