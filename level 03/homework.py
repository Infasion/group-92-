print(my_name + " ცხოვრობს " + favorite_city + "-ში უკვე " +
      str(years_in_city) + " წელია და მისი საყვარელი სეზონი " +
      best_season + "ია, სიყვარულის მსგავსად, ისიც " +
      str(loves_pizza) + "-ია პიცაზე.")


